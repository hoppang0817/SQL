--39 COUNT(*)일때 널값이 있어도 계산가능
SELECT DEPTNO, COUNT(*) AS "사원수" ,COUNT(COMM) AS "커미션 받은 사원수" FROM EMP GROUP BY DEPTNO;
--40 부서번호가 10은'총무부' 20은'개발부' 30은'영업부'
SELECT ENAME,DEPTNO,DECODE(DEPTNO,10,'총무부',
                                  20,'개발부',
                                  30,'영업부') 부서명 FROM EMP;
SELECT ENAME,DEPTNO, CASE WHEN DEPTNO=10 THEN '총무부' WHEN DEPTNO =20 THEN '개발부' ELSE '영업부' END AS 부서명 FROM EMP;
