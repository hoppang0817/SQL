--36. 사원수가 5명이 넘는 부서의 부서명과 사원수 조회
SELECT DEPT.DNAME , COUNT(*) FROM EMP,DEPT WHERE EMP.DEPTNO=DEPT.DEPTNO GROUP BY DEPT.DNAME HAVING COUNT(*)>5;
